void main(List<String> args) {
  ///Soal 1
  ///increment
  for (var i = 1; i <= 10; i++) {
    print(i);
  }
  //decrement
  for (var i = 10; i >= 1; i--) {
    print(i);
  }

  //soal 2
  ///increment
  int count1 = 1;
  while (count1 <= 10) {
    print(count1);
    count1++;
  }

  ///decrement
  int count = 10;

  while (count >= 1) {
    print(count);
    count--;
  }
}
